      character(len=*),intent(in)::name
      end subroutine
      end interface
      
      
      REAL(kind=DOUBLE)vector_temp(3)
      do j=1,zn_num
      zone_frags(j)=(0.0_DOUBLE)
      end do
      yhat(1)=(0.0_DOUBLE)
      yhat(2)=(1.0_DOUBLE)
      yhat(3)=(0.0_DOUBLE)
      xhat(1)=(1.0_DOUBLE)
      xhat(2)=(0.0_DOUBLE)
      xhat(3)=(0.0_DOUBLE)
      zhat(1)=(0.0_DOUBLE)
      zhat(2)=(0.0_DOUBLE)
      zhat(3)=(1.0_DOUBLE)
      dang=halfwidth
      x0(1)=points(1,0)
      x0(2)=points(2,0)
      x0(3)=points(3,0)
      
      x1(1)=points(1,1)
      x1(2)=points(2,1)
      x1(3)=points(3,1)
      
      deltax(1)=x1(1)-x0(1)
      deltax(2)=x1(2)-x0(2)
      deltax(3)=x1(3)-x0(3)
      
      vector_temp(1)=sqrt((deltax(1)**2+deltax(2)**2+deltax(3)**2))
      if(vector_temp(1).GT.(0.0_DOUBLE))continue
      delta_par(1)=((1.0_DOUBLE)/vector_temp(1))*deltax(1)
      delta_par(2)=((1.0_DOUBLE)/vector_temp(1))*deltax(2)
      delta_par(3)=((1.0_DOUBLE)/vector_temp(1))*deltax(3)
      
      if(((geometry_symmetry.EQ.1).OR.(geometry_symmetry.EQ.3).OR.(geome
     &try_symmetry.EQ.2)).AND.((deltax(1)*yhat(1)+deltax(2)*yhat(2)+delt
     &ax(3)*yhat(3)).EQ.(0.0_DOUBLE)))then
      option=1
      vector_temp(1)=delta_par(2)*yhat(3)-delta_par(3)*yhat(2)
      vector_temp(2)=delta_par(3)*yhat(1)-delta_par(1)*yhat(3)
      delta_perp(3)=delta_par(1)*yhat(2)-delta_par(2)*yhat(1)
      delta_perp(1)=vector_temp(1)
      delta_perp(2)=vector_temp(2)
      nang=max(1,nint((30._DOUBLE)*(dang*(1.8e2_DOUBLE)/atan2((0.0_DOUBL
     &E),-(1.0_DOUBLE)))))
      jmin=-nang
      jmax=nang
      kmin=1
      kmax=1
      else
      vector_temp(1)=delta_par(2)*xhat(3)-delta_par(3)*xhat(2)
      vector_temp(2)=delta_par(3)*xhat(1)-delta_par(1)*xhat(3)
      ux(3)=delta_par(1)*xhat(2)-delta_par(2)*xhat(1)
      ux(1)=vector_temp(1)
      ux(2)=vector_temp(2)
      vector_temp(1)=delta_par(2)*yhat(3)-delta_par(3)*yhat(2)
      vector_temp(2)=delta_par(3)*yhat(1)-delta_par(1)*yhat(3)
      uy(3)=delta_par(1)*yhat(2)-delta_par(2)*yhat(1)
      uy(1)=vector_temp(1)
      uy(2)=vector_temp(2)
      vector_temp(1)=delta_par(2)*zhat(3)-delta_par(3)*zhat(2)
      vector_temp(2)=delta_par(3)*zhat(1)-delta_par(1)*zhat(3)
      uz(3)=delta_par(1)*zhat(2)-delta_par(2)*zhat(1)
      uz(1)=vector_temp(1)
      uz(2)=vector_temp(2)
      if(sqrt((ux(1)**2+ux(2)**2+ux(3)**2)).GE.max(sqrt((uy(1)**2+uy(2)*
     &*2+uy(3)**2)),sqrt((uz(1)**2+uz(2)**2+uz(3)**2))))then
      u(1)=xhat(1)
      u(2)=xhat(2)
      u(3)=xhat(3)
      
      else if(sqrt((uy(1)**2+uy(2)**2+uy(3)**2)).GE.max(sqrt((ux(1)**2+u
     &x(2)**2+ux(3)**2)),sqrt((uz(1)**2+uz(2)**2+uz(3)**2))))then
      u(1)=yhat(1)
      u(2)=yhat(2)
      u(3)=yhat(3)
      
      else
      if(sqrt((uz(1)**2+uz(2)**2+uz(3)**2)).GT.max(sqrt((ux(1)**2+ux(2)*
     &*2+ux(3)**2)),sqrt((uy(1)**2+uy(2)**2+uy(3)**2))))continue
      u(1)=zhat(1)
      u(2)=zhat(2)
      u(3)=zhat(3)
      
      end if
      vector_temp(1)=delta_par(2)*u(3)-delta_par(3)*u(2)
      vector_temp(2)=delta_par(3)*u(1)-delta_par(1)*u(3)
      delta_perp_1(3)=delta_par(1)*u(2)-delta_par(2)*u(1)
      delta_perp_1(1)=vector_temp(1)
      delta_perp_1(2)=vector_temp(2)
      vector_temp(1)=sqrt((delta_perp_1(1)**2+delta_perp_1(2)**2+delta_p
     &erp_1(3)**2))
      if(vector_temp(1).GT.(0.0_DOUBLE))continue
      delta_perp_1(1)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_1(1)
      delta_perp_1(2)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_1(2)
      delta_perp_1(3)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_1(3)
      
      vector_temp(1)=delta_par(2)*delta_perp_1(3)-delta_par(3)*delta_per
     &p_1(2)
      vector_temp(2)=delta_par(3)*delta_perp_1(1)-delta_par(1)*delta_per
     &p_1(3)
      delta_perp_2(3)=delta_par(1)*delta_perp_1(2)-delta_par(2)*delta_pe
     &rp_1(1)
      delta_perp_2(1)=vector_temp(1)
      delta_perp_2(2)=vector_temp(2)
      vector_temp(1)=sqrt((delta_perp_2(1)**2+delta_perp_2(2)**2+delta_p
     &erp_2(3)**2))
      if(vector_temp(1).GT.(0.0_DOUBLE))continue
      delta_perp_2(1)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_2(1)
      delta_perp_2(2)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_2(2)
      delta_perp_2(3)=((1.0_DOUBLE)/vector_temp(1))*delta_perp_2(3)
      
      if((delta_par(1)*delta_perp_1(1)+delta_par(2)*delta_perp_1(2)+delt
     &a_par(3)*delta_perp_1(3)).LE.(5*EPSILON((0.0_DOUBLE))))continue
      if((delta_par(1)*delta_perp_2(1)+delta_par(2)*delta_perp_2(2)+delt
     &a_par(3)*delta_perp_2(3)).LE.(5*EPSILON((0.0_DOUBLE))))continue
      if((delta_perp_1(1)*delta_perp_2(1)+delta_perp_1(2)*delta_perp_2(2
     &)+delta_perp_1(3)*delta_perp_2(3)).LE.(5*EPSILON((0.0_DOUBLE))))co
     &ntinue
      if(algorithm.EQ.1)then
      option=2
      nang=6
      jmin=1
      jmax=2*nang
      kmin=1
      kmax=2*nang
      else if(algorithm.EQ.2)then
      option=3
      nang=6
      jmin=1
      jmax=2*nang
      kmin=1
      kmax=nang
      end if
      end if
      do j=jmin,jmax
      do k=kmin,kmax
      if(option.EQ.1)then
      ang=dang*REAL(j,DOUBLE)/REAL(nang,DOUBLE)
      v(1)=(cos(ang))*delta_par(1)
      v(2)=(cos(ang))*delta_par(2)
      v(3)=(cos(ang))*delta_par(3)
      
      v(1)=v(1)+delta_perp(1)*(sin(ang))
      v(2)=v(2)+delta_perp(2)*(sin(ang))
      v(3)=v(3)+delta_perp(3)*(sin(ang))
      
      if(algorithm.EQ.1)then
      mult=(1.0_DOUBLE)/((4._DOUBLE)*atan2((0.0_DOUBLE),-(1.0_DOUBLE))*R
     &EAL(2*nang+1,DOUBLE))
      else if(algorithm.EQ.2)then
      mult=(1.0_DOUBLE)/((2.0_DOUBLE)*atan2((0.0_DOUBLE),-(1.0_DOUBLE))*
     &*2*REAL(nang,DOUBLE))*sqrt((1.0_DOUBLE)-(REAL(j,DOUBLE)/REAL(nang,
     &DOUBLE))**2)
      else
      if('Unsupported averaging algorithm'.EQ.' ')continue
      end if
      else if(option.EQ.2)then
      v(1)=delta_par(1)
      v(2)=delta_par(2)
      v(3)=delta_par(3)
      
      v(1)=v(1)+delta_perp_1(1)*((REAL(j,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      v(2)=v(2)+delta_perp_1(2)*((REAL(j,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      v(3)=v(3)+delta_perp_1(3)*((REAL(j,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      
      v(1)=v(1)+delta_perp_2(1)*((REAL(k,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      v(2)=v(2)+delta_perp_2(2)*((REAL(k,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      v(3)=v(3)+delta_perp_2(3)*((REAL(k,DOUBLE)-(0.5_DOUBLE)-REAL(nang,
     &DOUBLE))*sin(dang)/REAL(nang,DOUBLE))
      
      omega=(4._DOUBLE)*atan((sin(dang))**2/(sqrt((1.0_DOUBLE)+(2.0_DOUB
     &LE)*(sin(dang))**2)))
      mult=(sin(dang))**2/((4._DOUBLE)*atan2((0.0_DOUBLE),-(1.0_DOUBLE))
     &*(REAL(nang,DOUBLE)**2*(sqrt((v(1)**2+v(2)**2+v(3)**2)))**3)*omega
     &)
      else
      if(option.EQ.3)continue
      theta_k=(REAL(k,DOUBLE)-(0.5_DOUBLE))*dang/REAL(nang,DOUBLE)
      phi_j=(REAL(j,DOUBLE)-(0.5_DOUBLE))*atan2((0.0_DOUBLE),-(1.0_DOUBL
     &E))/REAL(nang,DOUBLE)
      v(1)=delta_par(1)
      v(2)=delta_par(2)
      v(3)=delta_par(3)
      
      v(1)=v(1)+delta_perp_1(1)*(sin(theta_k)*cos(phi_j))
      v(2)=v(2)+delta_perp_1(2)*(sin(theta_k)*cos(phi_j))
      v(3)=v(3)+delta_perp_1(3)*(sin(theta_k)*cos(phi_j))
      
      v(1)=v(1)+delta_perp_2(1)*(sin(theta_k)*sin(phi_j))
      v(2)=v(2)+delta_perp_2(2)*(sin(theta_k)*sin(phi_j))
      v(3)=v(3)+delta_perp_2(3)*(sin(theta_k)*sin(phi_j))
      
      omega=(2.0_DOUBLE)*atan2((0.0_DOUBLE),-(1.0_DOUBLE))*((1.0_DOUBLE)
     &-cos(dang))
      mult=dang*sin(theta_k)/((4._DOUBLE)*REAL(nang,DOUBLE)**2*omega)
      end if
      pos_xx(1)=x0(1)
      pos_xx(2)=x0(2)
      pos_xx(3)=x0(3)
      
      cell_xx=locate_point(pos_xx,zone_xx)
      surface_xx=0
      if(zone_xx.GT.0)continue
      if(zone_type(zone_xx).NE.1.AND.zone_type(zone_xx).NE.2)then
      tmax=100
90000 continue
      t=(0.0_DOUBLE)
      done=track(0,tmax,pos_xx,v,cell_xx,t,pos_xx,cell_xx,surface_xx,cel
     &l_next_xx,sector_xx,sector_next_xx)
      if(cell_next_xx.EQ.0)go to 90010
      if(.NOT.done)then
      tmax=tmax-t
      if(surface_xx.NE.0)continue
      zone_next_xx=cells(4,cell_next_xx)
      if(surface_xx.NE.0)then
      surface_xx=0
      cell_xx=cell_next_xx
      zone_xx=zone_next_xx
      sector_xx=sector_next_xx
      end if
      if(zone_type(zone_xx).NE.1.AND.zone_type(zone_xx).NE.2)goto 90000
      else
      if('Viewing chord longer than expected'.EQ.' ')continue
      end if
      else
      end if
      tmax=100
90001 continue
      t=(0.0_DOUBLE)
      done=track(0,tmax,pos_xx,v,cell_xx,t,pos_xx,cell_xx,surface_xx,cel
     &l_next_xx,sector_xx,sector_next_xx)
      if(.NOT.done)continue
      if(zone_type(zone_xx).EQ.2)then
      zone_frags(zone_xx)=zone_frags(zone_xx)+(t*sqrt((v(1)**2+v(2)**2+v
     &(3)**2))*mult)
      end if
      tmax=tmax-t
      if(surface_xx.NE.0)continue
      zone_next_xx=cells(4,cell_next_xx)
      if(.NOT.done.AND.(zone_type(zone_next_xx).EQ.2.OR.zone_type(zone_n
     &ext_xx).EQ.1.OR.zone_type(zone_next_xx).EQ.4))then
      if(surface_xx.NE.0)then
      surface_xx=0
      cell_xx=cell_next_xx
      zone_xx=zone_next_xx
      sector_xx=sector_next_xx
      end if
      goto 90001
      end if
90010 continue
      end do
      end do
      do j=1,zn_num
      zone_frags(j)=zone_frags(j)/zone_volume(j)
      end do
      return
      end
