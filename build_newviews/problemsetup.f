      subroutine set_prod_desorption(ts,p)
      
      use pr_mod
      
      use pm_mod
      
      use el_mod
      
      use ps_mod
      
      implicit none
      integer,parameter::SINGLE=kind(0.0),DOUBLE=selected_real_kind(12)
      integer ts
      integer p
      external species_el_count
      integer species_el_count
      integer i,ic,j,count_ts,count_prod
      logical match
      integer generic_prod
      problem_pmi_num_arrange(problem_pmi_case_num(ts),ts)=0
      do i=1,pmi_product_num(p)
      generic_prod=generics(pmi_product(i,p))
      if(generic_prod.GT.0.AND.generic_prod.LE.num_generics)continue
      do j=1,num_equiv(generic_prod)
      match=.TRUE.
      do ic=1,el_num
      count_ts=species_el_count(problem_test_sp(ts),ic)
      count_prod=species_el_count(equivalents(j,generic_prod),ic)
      if(count_ts.GT.count_prod)match=.FALSE.
      end do
      if(match)then
      problem_pmi_num_arrange(problem_pmi_case_num(ts),ts)=problem_pmi_n
     &um_arrange(problem_pmi_case_num(ts),ts)+1
      problem_pmi_prod_mult(problem_pmi_num_arrange(problem_pmi_case_num
     &(ts),ts),problem_pmi_case_num(ts),ts)=(0.0_DOUBLE)
      problem_pmi_products(1,problem_pmi_num_arrange(problem_pmi_case_nu
     &m(ts),ts),problem_pmi_case_num(ts),ts)=equivalents(j,generic_prod)
      end if
      end do
      end do
      if(problem_pmi_num_arrange(problem_pmi_case_num(ts),ts).GT.0)conti
     &nue
      return
      end
