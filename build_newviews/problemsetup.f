      subroutine set_pmi_products(ts,p)
      
      use pr_mod
      
      use pm_mod
      
      implicit none
      integer,parameter::SINGLE=kind(0.0),DOUBLE=selected_real_kind(12)
      integer ts
      integer p
      integer i
      REAL(kind=DOUBLE)sum
      if(pmi_type(p).EQ.'reflection')then
      call set_prod_reflection(ts,p)
      else if(pmi_type(p).EQ.'adsorption')then
      call set_prod_adsorption(ts,p)
      else if(pmi_type(p).EQ.'desorption')then
      call set_prod_desorption(ts,p)
      else if(pmi_type(p).EQ.'sputter')then
      if('set_product for sputter not available'.EQ.' ')continue
      end if
      
      sum=(0.0_DOUBLE)
      do i=1,problem_pmi_num_arrange(problem_pmi_case_num(ts),ts)
      sum=sum+problem_pmi_prod_mult(i,problem_pmi_case_num(ts),ts)
      end do
      if(sum.GT.(0.0_DOUBLE))then
      do i=1,problem_pmi_num_arrange(problem_pmi_case_num(ts),ts)
      problem_pmi_prod_mult(i,problem_pmi_case_num(ts),ts)=problem_pmi_p
     &rod_mult(i,problem_pmi_case_num(ts),ts)/sum
      end do
      end if
      return
      end
