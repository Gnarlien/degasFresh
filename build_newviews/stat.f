     &inc_pf_dim,inc_ptr2full,inc_ps_dim,inc_ptr2short,base_compress,bas
     &e_wt_tot,base_size,base_dim,base_stat,base_pf_dim,base_ptr2full,ba
     &se_ps_dim,base_ptr2short,base_wt_dim,base_wt)
      
      implicit none
      integer,parameter::SINGLE=kind(0.0),DOUBLE=selected_real_kind(12)
      integer inc_size,inc_dim,inc_pf_dim,inc_ps_dim,inc_compress,base_c
     &ompress
      integer inc_ptr2full(0:inc_pf_dim-1),inc_ptr2short(0:inc_ps_dim-1)
      REAL(kind=DOUBLE)inc_wt
      REAL(kind=DOUBLE)inc_stat(0:1,0:inc_dim-1)
      integer base_size,base_dim,base_pf_dim,base_ps_dim,base_wt_dim
      integer base_ptr2full(0:base_pf_dim-1),base_ptr2short(0:base_ps_di
     &m-1)
      REAL(kind=DOUBLE)base_wt_tot
      REAL(kind=DOUBLE)base_stat(0:1,0:base_dim-1),base_wt(0:base_wt_dim
     &-1)
      integer i_inc,ifull,i_base
      REAL(kind=DOUBLE)b_wt,tot_wt,ma
      if(.NOT.(inc_wt.GE.(0.0_DOUBLE)))then
      write(0,*)' Bad value of inc_wt = ',inc_wt
      write(0,*)' inc_size = ',inc_size
      call flush(0)
      if(inc_wt.GE.(0.0_DOUBLE))continue
      end if
      if(base_wt_tot.GE.(0.0_DOUBLE))continue
      if(inc_size.GE.0)continue
      do i_inc=0,inc_size-1
      if(inc_compress.EQ.1)then
      ifull=inc_ptr2full(i_inc)
      else
      ifull=i_inc
      end if
      if(base_compress.EQ.1)then
      if(base_ptr2short(ifull).EQ.314159265)then
      base_size=base_size+1
      i_base=base_size-1
      base_ptr2full(i_base)=ifull
      base_ptr2short(ifull)=i_base
      base_stat(0,i_base)=(0.0_DOUBLE)
      base_stat(1,i_base)=(0.0_DOUBLE)
      base_wt(i_base)=(0.0_DOUBLE)
      else
      i_base=base_ptr2short(ifull)
      end if
      b_wt=base_wt(i_base)
      else
      i_base=ifull
      if(inc_compress.EQ.1)then
      b_wt=base_wt(i_base)
      else
      b_wt=base_wt_tot
      end if
      end if
      tot_wt=b_wt+inc_wt
      ma=base_stat(0,i_base)+(inc_stat(0,i_inc)-base_stat(0,i_base))*inc
     &_wt/tot_wt
      base_stat(1,i_base)=b_wt*base_stat(1,i_base)+inc_wt*inc_stat(1,i_i
     &nc)+(inc_stat(0,i_inc)-base_stat(0,i_base))*(inc_stat(0,i_inc)-ma)
     &*inc_wt
      if(tot_wt.GT.(0.0_DOUBLE).AND.base_stat(1,i_base).GT.(0.0_DOUBLE))
     &then
      base_stat(1,i_base)=base_stat(1,i_base)/tot_wt
      else
      base_stat(1,i_base)=(0.0_DOUBLE)
      end if
      base_stat(0,i_base)=ma
      if(inc_compress.EQ.1)base_wt(i_base)=tot_wt
      
      if(inc_compress.EQ.1)inc_ptr2short(ifull)=314159265
      end do
      if(inc_compress.EQ.1)then
      inc_size=0
      else
      do i_inc=0,inc_size-1
      inc_stat(0,i_inc)=(0.0_DOUBLE)
      inc_stat(1,i_inc)=(0.0_DOUBLE)
      end do
      end if
      base_wt_tot=base_wt_tot+(inc_wt)
      inc_wt=(0.0_DOUBLE)
      return
      end
      subroutine stat_wt_balance(wt_tot,size,dim,stat,wt_dim,wt)
      
      implicit none
      integer,parameter::SINGLE=kind(0.0),DOUBLE=selected_real_kind(12)
      integer size,dim,wt_dim
      REAL(kind=DOUBLE)wt_tot
      REAL(kind=DOUBLE)stat(0:1,0:dim-1),wt(0:wt_dim-1)
      integer i
      if(wt_tot.GT.(0.0_DOUBLE))continue
      if(wt_dim.EQ.dim)continue
      do i=0,size-1
      if(wt(i).GT.(0.0_DOUBLE).AND.wt(i).LT.wt_tot)then
      stat(1,i)=stat(1,i)*wt(i)/wt_tot+stat(0,i)**2*wt(i)*(wt_tot-wt(i))
     &/(wt_tot*wt_tot)
      stat(0,i)=stat(0,i)*(wt(i)/wt_tot)
      end if
      wt(i)=wt_tot
      end do
      return
      end
